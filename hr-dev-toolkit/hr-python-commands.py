# To check the version of python installed
# python --version

# To install a package
# python -m pip install package_name

# To list all the installed packages
# python -m pip list

# To show details of a package
# python -m pip show package_name

# To uninstall a package
# python -m pip uninstall package_name

# To run a python script
# python script_name.py

# To create a virtual environment
# python -m venv venv

# To activate a virtual environment (Linux/Mac)
# source venv/bin/activate

# To activate a virtual environment (Windows)
# venv\Scripts\activate

# To deactivate a virtual environment
# deactivate

# To install all the packages listed in the requirements.txt file
# pip install -r requirements.txt: 

# To list installed libraries by size
# pip-list --sort size --desc